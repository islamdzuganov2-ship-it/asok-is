"""seed_scale.py — DEPRECATED. Оставлен как совместимый вход, делегирует в seed_demo.

Раньше этот скрипт «проливал моки в оценку»: заливал 30 систем со СЛУЧАЙНЫМИ значениями
метрик (random) отдельным путём от сценарного сида. Это и была «плохая работа с
синтетическими данными»: два независимых генератора писали в оценку несогласованные
числа, а seed_demo затем вынужден был помечать эти 30 систем «мусорными» (JUNK_SYSTEM_NAMES)
и мягко их удалять. Итог — конфликтующая синтетика в базе.

Теперь синтетические данные заносятся ТОЛЬКО в оценку и ТОЛЬКО из одного источника —
сценарного сида seed_demo (детерминированные профили качества по 4 ИС × 6 кварталов, из
которых по формулам выводятся уровни, суждения и все агрегаты дашбордов). Чтобы старые
команды/скрипты не ломались, этот модуль сохраняет имя `seed_scale_async`, но делегирует
в единый источник.

Запуск (эквивалентно `python -m app.scripts.seed_demo`):
    docker compose exec backend python -m app.scripts.seed_scale
"""
import asyncio

from app.scripts.seed_demo import seed_data


async def seed_scale_async() -> None:
    """DEPRECATED-обёртка: единый источник синтетики — сценарный сид оценки (seed_demo)."""
    print(
        "seed_scale УСТАРЕЛ: случайная заливка 30 систем отключена. "
        "Синтетика заносится только в оценку через сценарный сид seed_demo — выполняю его."
    )
    await seed_data()
    print("Готово: оценка засеяна из единого сценарного источника (seed_demo).")


if __name__ == "__main__":
    asyncio.run(seed_scale_async())

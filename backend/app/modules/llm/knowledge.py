# Опоры управления ИТ (ISO/IEC 38500) для ролевых точек зрения.
GOVERNANCE_REFS: dict[str, str] = {
    "Стратегия": "ISO/IEC 38500: соответствие ИТ текущим и будущим потребностям бизнеса",
    "Соответствие": "ISO/IEC 38500: соблюдение обязательных норм, регламентов и внутренней политики",
    "Ответственность": "ISO/IEC 38500: распределение ответственности за решения и результаты по ИТ",
}


def relevant_terms(*text_blocks: str, limit: int = 6) -> str:
    """Подбирает определения характеристик, упомянутых во входных данных.

    Возвращает готовый блок «Справочник ISO 25010 (для раскрытия смысла, без новых чисел)»
    либо пустую строку, если ни одна характеристика не встретилась. Порядок — как в глоссарии
    (стабильно и предсказуемо), не по частоте (частота у мелких моделей шумная).
    """
    haystack = " ".join(text_blocks).lower()
    hits: list[str] = []
    for char, (definition, causes, impact) in GLOSSARY.items():
        if char.lower() in haystack:
            hits.append(
                f"- {char}: {definition}. "
                f"Типовые причины просадки: {causes}. "
                f"Последствия: {impact}."
            )
        if len(hits) >= limit:
            break
    if not hits:
        return ""
    return "Справочник ISO 25010 (для раскрытия смысла, без новых чисел):\n" + "\n".join(hits)

def _norm_key(value: str) -> str:
    """Нормализованный ключ для сопоставления имён (ё→е, нижний регистр, схлоп пробелов)."""
    return " ".join((value or "").lower().replace("ё", "е").split())


# Псевдонимы характеристик из Excel-импорта/легаси → каноническая характеристика модели.
# Решает DEF-02: имена из разных источников приводятся к единой номенклатуре 25010,
# чтобы дашборды/теплокарты не двоились (как в демо-моках).
_CHARACTERISTIC_ALIASES: dict[str, str] = {
    "надежность": "Надёжность",
    "безопасность": "Защищённость",
    "защищенность": "Защищённость",
    "эффективность": "Производительность",            # «Эффективность. Показатели временных…»
    "производительность": "Производительность",
    "пригодность для обслуживания": "Сопровождаемость",
    "сопровождаемость": "Сопровождаемость",
    "тестируемость": "Сопровождаемость",              # подхарактеристика, попавшая в характеристику
    "удобство использования": "Удобство использования",
    "функциональная пригодность": "Функциональная пригодность",
    "совместимость": "Совместимость",
    "переносимость": "Переносимость",
}

# Подготовленные нормализованные ключи: точное совпадение + префиксное (для «X. подробности»).
_ALIAS_BY_KEY: dict[str, str] = {_norm_key(a): c for a, c in _CHARACTERISTIC_ALIASES.items()}
_CANON_BY_KEY: dict[str, str] = {_norm_key(c): c for c in CHARACTERISTICS}


def canonical_characteristic(name: str) -> str | None:
    """Привести имя характеристики к канонической из модели 25010 или None, если не распознано.

    Сопоставление устойчиво к «ё/е», регистру, лишним пробелам и «хвостам» вида
    «Эффективность. Показатели временных Характеристик» (префиксное совпадение).
    """
    key = _norm_key(name)
    if not key:
        return None
    if key in _CANON_BY_KEY:
        return _CANON_BY_KEY[key]
    if key in _ALIAS_BY_KEY:
        return _ALIAS_BY_KEY[key]
    # Префиксное совпадение: имя начинается с известной характеристики/псевдонима.
    for alias_key, canon in {**_CANON_BY_KEY, **_ALIAS_BY_KEY}.items():
        if key.startswith(alias_key):
            return canon
    return None

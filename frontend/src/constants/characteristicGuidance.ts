// Нормализованный доступ (ё/е, регистр, пробелы/точки) — устойчив к вариантам написания.
const norm = (s: string) => (s || '').toLowerCase().replace(/ё/g, 'е').replace(/[.\s]/g, '');
const BY_NORM: Record<string, CharGuidance> = Object.fromEntries(
  Object.entries(CHARACTERISTIC_GUIDANCE).map(([k, v]) => [norm(k), v]),
);

/** Подсказка по характеристике с устойчивым к написанию поиском (undefined, если не найдена). */
export const guidanceFor = (characteristic?: string): CharGuidance | undefined =>
  (characteristic ? BY_NORM[norm(characteristic)] : undefined);
